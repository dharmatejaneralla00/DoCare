package com.marveltech.docare;

public class bedbookingHelperClass {
    String name,phone,uid,hospitalid,age,phonenumber;

    public bedbookingHelperClass(String name, String phone, String uid, String hospitalid, String age, String phonenumber) {
        this.name = name;
        this.phone = phone;
        this.uid = uid;
        this.hospitalid = hospitalid;
        this.age = age;
        this.phonenumber = phonenumber;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getHospitalid() {
        return hospitalid;
    }

    public void setHospitalid(String hospitalid) {
        this.hospitalid = hospitalid;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getPhonenumber() {
        return phonenumber;
    }

    public void setPhonenumber(String phonenumber) {
        this.phonenumber = phonenumber;
    }
}
